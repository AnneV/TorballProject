            <p>Connaissez-vous le torball ?</p>

            <p>Dérivé d'une rééducation de soldats allemands devenus aveugles 
            lors de la seconde guerre mondiale transformée par la suite en sport
            , le torball est apparu en France dans les années 70.</p>

            <p>Le torball, (on prononce 'torballe' comme pour le handball), est
            donc un sport de ballon pratiqué par les handicapés visuels hommes,
            femmes ou enfants.</p>

            <p>Crédit photo Jean-Raphaël DRAHI : voir la photo Il se joue avec deux
            équipes de trois joueurs dont l'objectif est de marquer un but à
            l'équipe adverse en lançant un ballon sonore uniquement à la main en
            le faisant passer sous trois cordes tendues en travers du
            terrain.</p>

            <p>Étant donnés les impératifs liés au silence lors d'un match, ce
            sport ne peut se pratiquer qu'à l'intérieur d'un gymnase.</p>

            <p>Le terrain est délimité par un rectangle de 16 mètres de Longueur
            sur 7 mètres de largeur. Les joueurs sont confrontés à de nombreux
            chocs, il est donc souhaitable de disposer d'un sol de type Taraflex
            ou d'un parquet.</p>

            <p>De part et d'autre du terrain on trouve un but qui en occupe toute
            la largeur, la ligne de fond correspondant à la ligne de but. Ce but
            est d'une longueur de 7 mètres et mesure 1,30 mètre de hauteur.</p>

            <p>Trois cordes sont tendues en travers du terrain. Elles sont à une
            hauteur de 40cm, et équipées de deux clochettes afin de les entendre
            lorsqu’on les touche. La corde centrale est juste au-dessus de la
            ligne médiane. De part et d'autre de celle-ci, on trouve deux autres
            cordes situées à 2 mètres. La zone délimitée par la ligne de but et
            la première corde représente la zone de jeu de chaque équipe. Elle
            mesure 7 mètres de large sur 6 mètres de long.</p>

            <p>Chaque équipe dispose, dans ce que l'on pourrait appeler« sa zone
            défensive , de 3 tapis mesurant 2 mètres de long sur 1 mètre de
            large. Fixés au sol par des adhésifs pour ne pas bouger lors des
            matchs, ils sont placés de telle sorte que leur longueur soit
            parallèle à la ligne de but. Le tapis du milieu au centre du terrain
            se situe à 20cm de la ligne de but, alors que l'arrière des tapis
            latéraux est aligné avec l'avant du tapis central. Les largeurs
            externes de ceux-ci bordant les lignes de touche.</p>

            <p>Crédit photo Jean-Raphaël DRAHI : voir la photo Ces tapis, outre
            qu'ils amortissent les contacts avec le sol, permettent aux joueurs
            de se positionner en défense et de s'orienter avant les tirs en se
            repérant avec les mains ou les pieds sur les bordures de ces
            rectangles.</p>

